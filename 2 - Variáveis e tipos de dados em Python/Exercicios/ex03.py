"""
Peça para o usuário digitar três valores inteiros e imprima a soma deles
"""
numero_um = int(input('Digite o primeiro número inteiro: '))
numero_dois = int(input('Digite o segundo número inteiro: '))
numero_tres = int(input('Digite o terceiro número inteiro: '))
resultado = numero_um + numero_dois + numero_tres

print(f'A soma dos três números digitados é: {resultado}')
