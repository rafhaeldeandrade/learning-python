"""
34.	Leia o valor do raio de um círculo e calcule e imprima a área do círculo correspondente.
A área do circulo é PI * raio2, considere PI = 3.141592.
"""
raio_circulo = float(input('Digite o raio do círculo: '))
area_circulo = 3.141592 * (raio_circulo ** 2)
print(f'A área do círculo é: {area_circulo}')
